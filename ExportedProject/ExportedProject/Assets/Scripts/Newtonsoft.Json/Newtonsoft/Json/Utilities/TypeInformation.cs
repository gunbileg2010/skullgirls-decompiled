using System;
using System.Runtime.CompilerServices;
using Newtonsoft.Json.Shims;

namespace Newtonsoft.Json.Utilities
{
	[Preserve]
	internal class TypeInformation
	{
		public Type Type
		{
			[CompilerGenerated]
			get
			{
				return null;
			}
			[CompilerGenerated]
			set
			{
			}
		}

		public PrimitiveTypeCode TypeCode
		{
			[CompilerGenerated]
			get
			{
				return default(PrimitiveTypeCode);
			}
			[CompilerGenerated]
			set
			{
			}
		}
	}
}
